def grader(score)
  if (score>1 || score<0.6)
    'F'
  elsif (score>=0.9 )
    'A'
  elsif (score>=0.8 && score<0.9)
    'B'
  elsif(score>=0.7 && score<0.8)
    'C'
  else
    'D'
  end
end
